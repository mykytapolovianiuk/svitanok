  USING (auth.role() = 'service_role');

-- Reviews Table
CREATE TABLE reviews (
  id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  product_id BIGINT REFERENCES products(id) ON DELETE CASCADE NOT NULL,
  user_id UUID REFERENCES auth.users,
  user_name TEXT NOT NULL,
  rating INTEGER CHECK (rating >= 1 AND rating <= 5) NOT NULL,
  comment TEXT,
  admin_reply TEXT,
  is_approved BOOLEAN DEFAULT FALSE,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc'::text, NOW())
);

-- Create index on product_id for faster queries
CREATE INDEX idx_reviews_product_id ON reviews(product_id);

-- Create index on is_approved for faster filtering
CREATE INDEX idx_reviews_is_approved ON reviews(is_approved);

-- Row Level Security for Reviews
ALTER TABLE reviews ENABLE ROW LEVEL SECURITY;

-- Anyone can view approved reviews
CREATE POLICY "Approved reviews are publicly readable"
  ON reviews FOR SELECT
  USING (is_approved = true);

-- Authenticated users can insert reviews
CREATE POLICY "Authenticated users can insert reviews"
  ON reviews FOR INSERT
  WITH CHECK (auth.role() = 'authenticated');

-- Only service role can update reviews (admin operations)
CREATE POLICY "Service role can update reviews"
  ON reviews FOR UPDATE
  USING (auth.role() = 'service_role');

-- Only service role can delete reviews (admin operations)
CREATE POLICY "Service role can delete reviews"
  ON reviews FOR DELETE
  USING (auth.role() = 'service_role');

